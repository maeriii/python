#arithmetic Operator
x = 24
y = 32
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - Returns the remind

#comparisom operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)# equal to
print(x != y)# not equal to

#Assignment Operators
a = 10
print(a)

a += 2
print(a)

#logical Operators
print(34 < 50 and 56 > 200)
print(34 < 50 or 56 > 200)
print(not(34 < 50 or 56 > 200))

